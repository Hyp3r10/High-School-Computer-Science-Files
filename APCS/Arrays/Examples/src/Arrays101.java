
public class Arrays101 {
	public static void main(String[] args) {
		String[] beatles = new String[4]; // 4 elements
		System.out.println(beatles[0] + ", " + beatles[1] + ", " + beatles[2] + ", " + beatles[3]);
		
		beatles[0] = "John";
		beatles[1] = "Paul";
		beatles[2] = "George";
		beatles[3] = "Pete";
		
		System.out.println(beatles[0] + ", " + beatles[1] + ", " + beatles[2] + ", " + beatles[3]);
	}
}
