public class Operators101
{
    public static void main(String[] args)
    {
        int num1; // Declaring variable 
        num1 = 5; // = is called the assignment operator. Initializing the variable.
        
        int num2 = 12; // It is possiable (and preferable) to declare variables in the same command statement.
        
        
        System.out.println(num1);
        
        num1 = 8;
        
        System.out.println(num1);
        
        System.out.println(num2);
        System.out.println("num2");
    }
}