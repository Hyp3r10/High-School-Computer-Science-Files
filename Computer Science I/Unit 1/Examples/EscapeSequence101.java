public class EscapeSequence101
{
    public static void main (String[] args)
    {
        System.out.println("Patric Henry said, \"Give me liberty, or give me death!\" in 1775.");
        System.out.println("The file is located at c:\\myFiles\\myDoc.doc");
        System.out.println("The file is located at\nc:\\myFiles\\myDoc.doc");
        System.out.println("\n" + "The file is located at c:\\myFiles\\myDoc.doc" + "\n");
        System.out.println("\n\nThe file is located at c:\\myFiles\\myDoc.doc");
        System.out.println("Name\tAge\tDate");
        System.out.println("I like this\rNo start over");
    }
}